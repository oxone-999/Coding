void solve()
{
    lli n;
    cin>>n;
    
    vector<lli> vec(n,0);
    for(auto &it:vec){
        cin>>it;
    }

    map<lli,lli> mp;
    vector<lli> norm(n,1);
    vector<lli> invnorm(n,1);

    for(lli i=0;i<n;i++){
        lli x = vec[i];

        for(lli j=2;j*j<=x;j++){
            lli cnt = 0;
            while(x % j == 0){
                // cout<<":"<<x<<": "<<j<<" ";
                cnt++;
                x /= j;
            }
            cnt %= 3;
            norm[i] *= binpow(j,cnt);
            invnorm[i] *= binpow(j,(3-cnt)%3);
        }
        if(x > 1){
            norm[i] *= x;
            invnorm[i] *= (x*x);
        }
        mp[invnorm[i]]++;
    }

    // pr(norm);
    // pr(invnorm);

    lli ans = 0;

    for(lli i=0;i<n;i++){
        if(mp.count(invnorm[i])) mp[invnorm[i]]--;
        ans += mp[norm[i]];
    }

    cout<<ans<<endl;
}

